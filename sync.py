"""
Connect Happy Valley GeoDirectory Sync
Pulls business listings from WordPress GeoDirectory REST API and upserts to Supabase.

This is a standalone job (GitHub Actions, hourly) and is deliberately decoupled
from the iOS app: it only writes to the Supabase `places` table; the iOS app
reads from that table unchanged.

iOS CONTRACT (why transform_listing looks the way it does)
----------------------------------------------------------
The iOS `Place` model (ConnectHappyValley/Models/Place.swift) decodes the whole
Discover result set all-or-nothing and treats several columns as REQUIRED /
non-optional:
  - `type`       -> a PlaceType enum (restaurant|bar|cafe|hotel|experience|venue|shop)
  - `name`
  - `created_at`, `updated_at`
  - `id`         (uuid; generated by the DB default, never set here)
And it only ever returns rows where `is_active = true`.

So every row we write MUST carry a valid `type`, `is_active = true`, and
`created_at`/`updated_at`, or synced rows will either be invisible or — worse —
break decoding for the entire Discover tab (including the curated rows). The
transform below guarantees all of that. Listings whose category has no iOS type
mapping are SKIPPED and logged, never written with a null type.

The app reads `image_url` and `is_featured`; GeoDirectory's native names are
`photo_url`/`featured`. We populate BOTH conventions so the data is correct for
the app today and self-describing in the table.
"""

