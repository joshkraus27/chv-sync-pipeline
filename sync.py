def validate_env(dry_run: bool = False):
    """Ensure required environment variables are set.

    In dry-run mode we never touch Supabase, so its credentials aren't required
    — a dry run only needs GeoDirectory access.
    """
    if dry_run:
        return
    missing = []
    if not SUPABASE_URL:
        missing.append("SUPABASE_URL")
    if not SUPABASE_SERVICE_ROLE_KEY:
        missing.append("SUPABASE_SERVICE_ROLE_KEY")
    if missing:
        log.error(f"Missing required environment variables: {missing}")
        sys.exit(1)


def validate_category_config():
    """Surface (without crashing) any gaps in the bucket config.

    Every allowed bucket needs a default PlaceType and a display name, or
    matching listings get skipped / log as "Unknown". Logging this at startup
    keeps the structures in config/categories.py honest.
    """
    missing_type = ALLOWED_PARENT_IDS - set(PARENT_ID_TO_PLACE_TYPE)
    if missing_type:
        log.warning(
            f"{len(missing_type)} allowed buckets have NO default PlaceType — "
            f"their listings will be SKIPPED: {sorted(missing_type)}"
        )
    bad_type = {b: t for b, t in PARENT_ID_TO_PLACE_TYPE.items() if t not in VALID_PLACE_TYPES}
    if bad_type:
        log.warning(f"Bucket defaults with invalid PlaceType values: {bad_type}")
    missing_name = ALLOWED_PARENT_IDS - set(PARENT_BUCKET_NAMES)
    if missing_name:
        log.warning(f"Allowed buckets with no display name: {sorted(missing_name)}")


def get_auth_header() -> Optional[tuple]:
    """Return auth tuple for requests, or None if no auth configured."""
    if not GEODIR_BASIC_AUTH:
        return None
    if ":" not in GEODIR_BASIC_AUTH:
        log.error("GEODIR_BASIC_AUTH must be in format 'username:password'")
        sys.exit(1)
    user, pw = GEODIR_BASIC_AUTH.split(":", 1)
    return (user, pw)


def fetch_listings_page(page: int, auth: Optional[tuple]) -> list:
    """Fetch a single page of GeoDirectory listings."""
    url = f"{GEODIR_BASE_URL}/wp-json/geodir/v2/places"
    params = {"per_page": PER_PAGE, "page": page}
    log.info(f"Fetching page {page} from {url}")

    response = requests.get(
        url,
        params=params,
        auth=auth,
        timeout=REQUEST_TIMEOUT,
        headers={"Accept": "application/json"},
    )

    if response.status_code == 400:
        # WordPress returns 400 when page exceeds available pages
        log.info(f"Page {page} returned 400 — likely end of results")
        return []

    response.raise_for_status()
    return response.json()


def fetch_all_categories(auth: Optional[tuple]) -> dict:
    """Fetch all categories once and return an {id: parent} map.

    Listings embed subcategory ids but not reliably their parent, so we resolve
    parent via this map. It's static during a run, so we fetch it once up front.
    """
    cat_url = f"{GEODIR_BASE_URL}/wp-json/geodir/v2/places/categories"
    id_to_parent = {}
    for page in range(1, 20):  # ~700 categories max at 100/page
        response = requests.get(
            cat_url,
            params={"per_page": 100, "page": page},
            auth=auth,
            timeout=REQUEST_TIMEOUT,
            headers={"Accept": "application/json"},
        )
        if response.status_code == 400:
            break
        response.raise_for_status()
        cats = response.json()
        if not cats:
            break
        for cat in cats:
            try:
                id_to_parent[int(cat["id"])] = int(cat["parent"])
            except (KeyError, TypeError, ValueError):
                continue
    log.info(f"Loaded {len(id_to_parent)} categories with parent mappings")
    return id_to_parent


def bucket_id_for_category(cat: dict, id_to_parent: dict) -> Optional[int]:
    """Resolve the allowed top-level bucket id for a single category, or None.

    Handles both shapes: a listing tagged directly with a top-level bucket
    (rare), and the common case of a subcategory whose parent is a bucket.
    """
    cat_id = cat.get("id")
    if cat_id is None:
        return None
    try:
        cat_id = int(cat_id)
    except (TypeError, ValueError):
        return None
    # The category IS itself a whitelisted top-level bucket.
    if cat_id in ALLOWED_PARENT_IDS:
        return cat_id
    # ...or its parent is a whitelisted bucket.
    parent_id = id_to_parent.get(cat_id)
    if parent_id in ALLOWED_PARENT_IDS:
        return parent_id
    return None


def included_category(listing: dict, id_to_parent: dict) -> Optional[tuple]:
    """Return (category dict, bucket_id) for the listing's first allowed category.

    A listing can carry several categories; we use the first one — in API order —
    that resolves to an allowed bucket, and base both the include decision and
    the type/category fields on it, so filtering and typing never disagree.
    """
    for cat in listing.get("post_category", []) or []:
        bucket_id = bucket_id_for_category(cat, id_to_parent)
        if bucket_id is not None:
            return cat, bucket_id
    return None


def should_include_listing(listing: dict, id_to_parent: dict) -> bool:
    """Whitelist check — does this listing belong to an allowed top-level bucket?"""
    return included_category(listing, id_to_parent) is not None


def resolve_place_type(cat_name: Optional[str], bucket_id: int) -> Optional[str]:
    """Two-tier type resolution: name refinement over the guaranteed bucket default.

    Returns a valid iOS PlaceType, or None only if the bucket has no default
    (a config error caught by validate_category_config()).
    """
    refined = place_type_for_category_name(cat_name)
    if refined in VALID_PLACE_TYPES:
        return refined
    return PARENT_ID_TO_PLACE_TYPE.get(bucket_id)


def clean_text(value: Optional[str]) -> str:
    """Decode HTML entities and repair common mojibake.

    Used for display strings (names, descriptions, address parts, category
    names) — NOT for URLs or contact fields, which must stay byte-exact.
    """
    if not value:
        return ""

    # Step 1: HTML-unescape (handles &#038; &amp; &#8217; &#8211; etc.)
    cleaned = html.unescape(value)

    # Step 2: Repair double-encoded UTF-8 mojibake. When valid UTF-8 was decoded
    # as a single-byte codec and re-encoded as UTF-8 (a classic WordPress bug),
    # round-tripping <codec> -> utf-8 undoes it. We try latin-1 first ("KafÃ©" ->
    # "Kafé") then cp1252, which is needed for the curly-quote/dash family
    # ("â€™" -> "'", "â€“" -> "–") whose bytes fall in 0x80-0x9F and aren't in
    # latin-1. Each attempt is guarded; a string that's already clean raises and
    # is kept as-is.
    for codec in ("latin-1", "cp1252"):
        try:
            cleaned = cleaned.encode(codec).decode("utf-8")
            break
        except (UnicodeEncodeError, UnicodeDecodeError):
            continue

    return cleaned.strip()


def _is_mshots_url(url: str) -> bool:
    """True for WordPress mshots auto-screenshots (s.wordpress.com/mshots/...).

    These are placeholder screenshots of a business's website, not real photos,
    so we treat them as "no photo" rather than write them into image_url/photo_url.
    """
    return "s.wordpress.com/mshots" in url.lower()


def extract_photo_url(listing: dict) -> Optional[str]:
    """Pull the best available real photo URL from a GeoDirectory listing.

    Priority: featured_image.src > images[0].src > None. We do NOT gate on `id`
    (mshots screenshots always carry id=0). We DO skip s.wordpress.com/mshots
    screenshots: a real images[0] can win over an mshots featured_image, and if
    mshots is the only candidate the photo is left null.
    """
    candidates = []

    featured = listing.get("featured_image")
    if isinstance(featured, dict):
        src = featured.get("src")
        if src and isinstance(src, str) and src.strip():
            candidates.append(src.strip())

    images = listing.get("images")
    if isinstance(images, list) and images:
        first = images[0]
        if isinstance(first, dict):
            src = first.get("src")
            if src and isinstance(src, str) and src.strip():
                candidates.append(src.strip())

    for src in candidates:
        if not _is_mshots_url(src):
            return src

    return None


# ---------------------------------------------------------------------------
# Post-transform exclusion filters. These run on the CLEANED record (name,
# category_name, description already HTML-unescaped + mojibake-repaired by
# clean_text), which is why they live after transform_listing rather than
# alongside the raw-listing whitelist.
# ---------------------------------------------------------------------------

# Confirmed adult venues, matched by exact (normalized) name. Venue names are
# REDACTED in this public copy. The category/phrase checks below independently
# catch all of them too — each carries category_name "Adult Entertainment" —
# so this is a belt-and-braces anchor, deliberately narrow (exact equality,
# not substring) so ordinary businesses that happen to share a word with a
# venue name are never caught.
ADULT_EXACT_NAMES = {
    "redacted-venue-1",
    "redacted-venue-2",
    "redacted-venue-3",
    "redacted-venue-4",
}

# Whole-phrase signals. "adult entertainment" is the GeoDirectory subcategory
# these venues sit under; the two club phrases are unambiguous. No loose single
# words (exotic/topless/adult-alone) — they risk false positives and caught
# nothing in the live data.
ADULT_PHRASES = (
    "adult entertainment",
    "gentlemen's club",
    "strip club",
)


def _normalize_for_match(text: Optional[str]) -> str:
    """Casefold, strip, and straighten curly apostrophes for tolerant matching."""
    if not text:
        return ""
    return text.replace("’", "'").replace("‘", "'").casefold().strip()


def is_adult_listing(record: dict) -> bool:
    """True if a transformed record is an adult-entertainment venue (excluded).

    Precision-biased: exact-name match on the confirmed venues, plus whole
    phrases in name/category/description. See ADULT_EXACT_NAMES / ADULT_PHRASES.
    """
    if _normalize_for_match(record.get("name")) in ADULT_EXACT_NAMES:
        return True
    blob = " ".join(
        _normalize_for_match(record.get(field))
        for field in ("name", "category_name", "description")
    )
    return any(phrase in blob for phrase in ADULT_PHRASES)


def is_unrecoverable_name(name: Optional[str]) -> bool:
    """True if a name is encoding garbage that clean_text couldn't repair.

    Conservative — flags only names that are genuinely unusable:
      - empty/whitespace after cleaning,
      - contain the Unicode replacement char (U+FFFD),
      - contain a C0/C1 control char (the residue of a failed cp1252 round-trip
        on double-encoded CJK — the signature of the mojibake we can't fix), or
      - contain no ASCII letter at all (pure non-Latin garbage).
    Legitimate accented / Vietnamese / Latin+CJK names all keep at least one
    ASCII letter and carry no control chars, so they survive.
    """
    if not name:
        return True
    s = name.strip()
    if not s:
        return True
    if "�" in s:
        return True
    for ch in s:
        if unicodedata.category(ch) == "Cc" and ch not in "\t\n\r":
            return True
    if not any(("a" <= c <= "z") or ("A" <= c <= "Z") for c in s):
        return True
    return False


